a=10
b=15

#Addition
print(a+b)

#Subtraction
print(a-b)

#Multiplication
print(a*b)

#Integer Division
print(a/b)

#Modulus Operator or Remainder
print(a%b)

#Comparison Operator
c=10.0
d=10

print(f"{c} is equals to {b} {c==d}")
print(f"{c} is not equal to {b} {c!=d}")
print(f"{c} is greater than or equals to {d} {c>=d}")
print(f"{c} is less than or equals to {d} {c<=d}")

#Logical Operators
e=7
print(e>4 and e<7)
print(e>4 or e<7)